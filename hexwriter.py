code = bytearray([
    0x60, 0x05,       # 0x200: LD V0, 0x05 (X=5)
    0x61, 0x05,       # 0x202: LD V1, 0x05 (Y=5)
    0x62, 0x08,       # 0x204: LD V2, 0x08 
    0xA2, 0x10,       # 0x206: LD I, 0x210 
    0xD0, 0x28,       # 0x208: DRW V0, V1, 0x8
    0x12, 0x08,       # 0x20A: JP 0x208 (endless loop)
    
    # Vertical line sprite
    0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF
])

with open("test_draw.bin", "wb") as f:
    f.write(code)

